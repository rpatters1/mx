"""Deterministic file output for the emit stage.

A backend renders a target into a manifest (relative path -> content); the
writer makes the output directory match it exactly:

  - files are written only when their content actually changed, so build
    systems see stable mtimes across no-op regenerations;
  - files the generator wrote on a previous run but that left the manifest
    (a type was renamed, a partition changed) are pruned -- but only files
    carrying the generated-code marker are ever deleted, so a hand-written
    file accidentally placed in the output directory is reported, never
    removed.

Every generated file must carry the marker near the top; render() output is
checked here so a backend cannot silently emit unmarked (unprunable) files.
"""

from __future__ import annotations

from dataclasses import dataclass, field
from pathlib import Path

# The substrings that identify a file as generator-owned. The Go convention
# (^// Code generated .* DO NOT EDIT\.$) is satisfied when a backend puts
# banner() in a line comment, and the prune check matches any comment syntax.
_MARK_PREFIX = "Code generated by gen"
_MARK_SUFFIX = "DO NOT EDIT."

# How far into a file the marker must appear (license headers may precede it).
_MARK_WINDOW = 512


def banner(source: str) -> str:
    """The marker sentence a backend wraps in its comment syntax, e.g.
    `// {banner(plates.source)}` for Go."""
    return f"{_MARK_PREFIX} from {source}. {_MARK_SUFFIX}"


def is_generated(content: str) -> bool:
    head = content[:_MARK_WINDOW]
    return _MARK_PREFIX in head and _MARK_SUFFIX in head


@dataclass
class EmitResult:
    out_dir: Path
    written: list[str] = field(default_factory=list)  # created or changed
    unchanged: list[str] = field(default_factory=list)
    pruned: list[str] = field(default_factory=list)  # stale generated files
    foreign: list[str] = field(default_factory=list)  # unmarked files left alone

    def summary(self) -> str:
        parts = [f"{len(self.written)} written", f"{len(self.unchanged)} unchanged"]
        if self.pruned:
            parts.append(f"{len(self.pruned)} pruned")
        if self.foreign:
            parts.append(f"{len(self.foreign)} foreign file(s) left alone")
        return f"{self.out_dir}: " + ", ".join(parts)


def write_files(out_dir: Path, files: dict[str, str]) -> EmitResult:
    """Make out_dir contain exactly the manifest's generated files."""
    for rel, content in files.items():
        path = Path(rel)
        if path.is_absolute() or ".." in path.parts:
            raise ValueError(f"backend produced an unsafe output path: {rel}")
        if not is_generated(content):
            raise ValueError(f"backend produced a file without the generated marker: {rel}")

    result = EmitResult(out_dir=out_dir)
    out_dir.mkdir(parents=True, exist_ok=True)

    for rel in sorted(files):
        target = out_dir / rel
        content = files[rel]
        try:
            if target.exists() and target.read_text(encoding="utf-8") == content:
                result.unchanged.append(rel)
                continue
        except (UnicodeDecodeError, OSError):
            pass  # whatever is there, it is not our content: overwrite it
        target.parent.mkdir(parents=True, exist_ok=True)
        target.write_text(content, encoding="utf-8")
        result.written.append(rel)

    keep = {str(Path(rel)) for rel in files}
    for path in sorted(out_dir.rglob("*")):
        if not path.is_file():
            continue
        rel = str(path.relative_to(out_dir))
        if rel in keep:
            continue
        try:
            owned = is_generated(path.read_text(encoding="utf-8"))
        except (UnicodeDecodeError, OSError):
            owned = False
        if owned:
            path.unlink()
            result.pruned.append(rel)
        else:
            result.foreign.append(rel)
    return result
